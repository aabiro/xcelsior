function escapeValue(v) {
  return `"${v.replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
}

function patchLocale(locale, additions) {
  const path = join(ROOT, `${locale}.ts`);
  let src = readFileSync(path, "utf8");
  const existing = new Set();
  const re = /"([^"]+)":/g;
  let m;
  while ((m = re.exec(src))) existing.add(m[1]);

  const lines = [];
  for (const [key, value] of Object.entries(additions)) {
    if (existing.has(key)) continue;
    lines.push(`  "${key}": ${escapeValue(value)},`);
  }
  if (lines.length === 0) {
    console.log(`${locale}: nothing to add`);
    return;
  }

  const marker = "\n};\n\nexport default";
  const idx = src.lastIndexOf(marker);
  if (idx === -1) throw new Error(`Could not find export marker in ${locale}.ts`);
  const block = `\n  /* ── Patched missing keys ─────────────────────────────────────────── */\n${lines.join("\n")}\n`;
  src = src.slice(0, idx) + block + src.slice(idx);
  writeFileSync(path, src);
  console.log(`${locale}: added ${lines.length} keys`);
}

